def _build_pairs(*tables):
    """合并多张表并按 key 长度降序，避免短 key 吃掉长 key。"""
    merged = {}
    for t in tables:
        merged.update(t)
    return sorted(merged.items(), key=lambda kv: -len(kv[0]))


_ALL_PAIRS = _build_pairs(_YAKU, _SCORE_CLASS, _ENDING, _DAN, _RULE, _MISC)


def localize(s: str) -> str:
    """日文字符串 -> 简体。非字符串原样返回。"""
    if not isinstance(s, str):
        return s
    for old, new in _ALL_PAIRS:
        if old in s:
            s = s.replace(old, new)
    return s


