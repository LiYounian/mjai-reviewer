SCORE_CLASS_KEYS = ("役满", "三倍满", "倍满", "跳满", "切上满贯", "满贯", "流满")


def detect_score_class(yaku_strs):
    """从一组役名字符串里识别打点级别 (已归化或未归化都吃)。

    返回 ("役满"/"三倍满"/.../"满贯"/None, 飜数 or None)。
    用于 stats 端给打点分布分桶。
    """
    joined = " ".join(localize(y) for y in yaku_strs if isinstance(y, str))
    for k in SCORE_CLASS_KEYS:
        if k in joined:
            return k
    # 没打到满贯档：找最大番数 (X番) 累加
    import re
    nums = [int(x) for x in re.findall(r"(\d+)番", joined)]
    if nums:
        return None  # 让 stats 自己按番数算
    return None


def total_han(yaku_strs):
    """累加役名串里的番数。

    天凤 yaku[] 的第 0 项是总打点串 ('30符3飜3900点' / '満貫8000点' / '跳満...点')，
    其余才是各役条目 ('立直(1飜)' / 'ドラ(2飜)')。只数后面，避免和总打点串里的"X番"重复。
    """
    import re
    n = 0
    for y in yaku_strs[1:]:
        if not isinstance(y, str):
            continue
        for m in re.findall(r"(\d+)番", localize(y)):
            n += int(m)
    return n
